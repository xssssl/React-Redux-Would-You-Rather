function generateUID(): string {
  return Math.random().toString(36).substring(2, 15) + Math.random().toString(36).substring(2, 15)
}

export function _getUsers(): Promise<Users> {
  return new Promise((res, rej) => {
    setTimeout(() => {
      res({...users})
    }, 1500 + Math.round(Math.random() * 1000))
  })
}

export function _getQuestions(): Promise<Questions> {
  return new Promise((res, rej) => {
    setTimeout(() => res({...questions}), 1500 + Math.round(Math.random() * 1000))
  })
}

export function formatQuestion ({ optionOneText, 
                           optionTwoText, 
                           author }: CreateQuestionRequest): Question {
  return {
    id: generateUID(),
    timestamp: Date.now(),
    author,
    optionOne: {
      votes: [],
      text: optionOneText,
    },
    optionTwo: {
      votes: [],
      text: optionTwoText,
    }
  }
}

export function _saveQuestion (question: CreateQuestionRequest): Promise<Question> {
  return new Promise((res, rej) => {
    const authedUser = question.author;
    const formattedQuestion: Question = formatQuestion(question);

    setTimeout(() => {
      questions = {
        ...questions,
        [formattedQuestion.id]: formattedQuestion
      }
      
      users = {
        ...users,
        [authedUser]: {
          ...users[authedUser],
          questions: users[authedUser].questions.concat([formattedQuestion.id])
        }
      }

      res(formattedQuestion)
    }, 1000 + Math.round(Math.random() * 1000))
  })
}

export function _saveQuestionAnswer ({ authedUser, qid, answer }: CreateAnswerRequest): Promise<void> {
  return new Promise((res, rej) => {
    setTimeout(() => {
      users = {
        ...users,
        [authedUser]: {
          ...users[authedUser],
          answers: {
            ...users[authedUser].answers,
            [qid]: answer
          }
        }
      }

      questions = {
        ...questions,
        [qid]: {
          ...questions[qid],
          [answer]: {
            ...questions[qid][answer],
            votes: questions[qid][answer].votes.concat([authedUser])
          }
        }
      }
      res()
    }, 500 + Math.round(Math.random() * 1000))
  })
}

export function _userAuthentication ({ id, password }: UserAuthenticationRequest): Promise<string> {
  return new Promise((res, rej) => {
    setTimeout(() => {
      if(password && Object.keys(users).indexOf(id) >= 0) res(generateUID())
      else rej('Invalid user')
    }, 1000 + Math.round(Math.random() * 1000))
  }) 
}
